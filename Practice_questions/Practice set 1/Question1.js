// ** Creat a variable of type string and try to add two numbers after that in same variable.

let s = "Babita Thakur"
console.log("Type of s before initialization modification:", typeof s)
s = 20 + 26
console.log(s)
console.log("Type of s after initialization modification:", typeof s)

// ! At compile time we declared variable type "String" but we initialize our variable by adding two integers that's why at run time data_type of varaible 's' changes from string to int(or number).